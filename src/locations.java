
public class locations {
	private int loctA;
	private int loctB;
	public locations(int loctA, int loctB) {
		this.loctA=loctA;
		this.loctB=loctB;
		
	}
	public int getLoctA() {
		return loctA;
	}
	public void setLoctA(int loctA) {
		this.loctA = loctA;
	}
	public int getLoctB() {
		return loctB;
	}
	public void setLoctB(int loctB) {
		this.loctB = loctB;
	}
	
}
